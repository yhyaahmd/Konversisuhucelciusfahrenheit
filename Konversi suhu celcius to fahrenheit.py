# Konversi suhu dari celcius ke fahrenheit
# rumus celcius to fahrenheit 9/5*c+32

print("Conversi suhu celcius to fahrenheit")
celcius = int(input("Masukkan Suhu celcius :"))
r1 = 9/5
r2 = 32

rumuscelciustofahrenheit = r1*celcius #rumus 9/5*c
rumus2 = rumuscelciustofahrenheit+r2 #hasil rumus9/5*c lalu ditambah 32

print("Maka suhu fahrenheitnya adalah", rumus2,"*f")